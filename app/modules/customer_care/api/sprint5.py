# ============================================================== Bulk KB CSV
@router.post(
    "/kb/import-csv",
    summary="Bulk-ingest KB documents from CSV (columns: title, body[, language, source_url])",
    dependencies=[Depends(requires_permission(_RAG_ADMIN))],
)
async def kb_import_csv(
    uow: Annotated[UnitOfWork, Depends(get_uow)],
    principal: Annotated[Principal, Depends(get_current_principal)],
    file: UploadFile = File(...),
) -> dict[str, Any]:
    raw = await file.read()
    if len(raw) > 2 * 1024 * 1024:
        raise ValidationError("CSV too large — 2 MB max")
    try:
        text_str = raw.decode("utf-8", errors="replace")
    except Exception:
        raise ValidationError("CSV must be UTF-8")
    reader = csv.DictReader(io.StringIO(text_str))
    rows = list(reader)
    if not rows:
        raise ValidationError("CSV is empty or missing header row")
    # Reuse the KB ingest flow inline (chunk + sync embedding via worker)
    from app.modules.customer_care.api.kb_csat import _simple_chunk
    import hashlib
    from sqlalchemy.dialects.postgresql import insert as _pg_insert
    from app.modules.customer_care.models import CCKnowledgeChunk
    ingested = 0
    skipped_dup = 0
    skipped_invalid = 0
    async with uow.transactional() as session:
        for r in rows:
            title = (r.get("title") or "").strip()
            body = (r.get("body") or "").strip()
            if not title or not body:
                skipped_invalid += 1
                continue
            digest = hashlib.sha256(body.encode("utf-8")).hexdigest()
            dup = (
                await session.execute(
                    select(CCKnowledgeDocument).where(
                        CCKnowledgeDocument.body_sha256 == digest,
                        CCKnowledgeDocument.is_active.is_(True),
                    )
                )
            ).scalar_one_or_none()
            if dup:
                skipped_dup += 1
                continue
            doc = CCKnowledgeDocument(
                title=title[:255], body=body, body_sha256=digest,
                source_type=(r.get("source_type") or "text")[:40],
                source_url=(r.get("source_url") or None),
                language=(r.get("language") or None),
            )
            session.add(doc)
            await session.flush()
            chunks = _simple_chunk(body)
            for i, ch in enumerate(chunks):
                session.add(
                    CCKnowledgeChunk(
                        document_id=doc.id, position=i,
                        text_body=ch,
                        text_hash=hashlib.sha256(ch.encode("utf-8")).hexdigest(),
                        token_count=len(ch) // 4,
                    )
                )
            doc.chunk_count = len(chunks)
            doc.indexed_at = datetime.now(timezone.utc)
            ingested += 1
        await record_audit(
            actor=principal,
            action="customer_care.kb.bulk_import",
            resource_type="cc_knowledge_documents",
            metadata={
                "ingested": ingested,
                "skipped_dup": skipped_dup,
                "skipped_invalid": skipped_invalid,
            },
        )
    return {
        "ingested": ingested,
        "skipped_duplicates": skipped_dup,
        "skipped_invalid": skipped_invalid,
        "total_rows": len(rows),
        "note": "Embeddings will be generated by the cc_kb_embed_worker cron",
    }


# ============================================================== KB doc update
class KbDocPatch(StrictModel):
    title: str | None = Field(default=None, min_length=1, max_length=255)
    body: str | None = Field(default=None, min_length=1, max_length=200_000)
    is_active: bool | None = None
    language: str | None = Field(default=None, max_length=8)


@router.patch(
    "/kb/documents/{doc_id}",
    summary="Update a KB document (re-chunks + clears stale embeddings if body changes)",
    dependencies=[Depends(requires_permission(_RAG_ADMIN))],
)
async def kb_patch(
    doc_id: Annotated[UUID, Path(...)],
    body: KbDocPatch,
    uow: Annotated[UnitOfWork, Depends(get_uow)],
    principal: Annotated[Principal, Depends(get_current_principal)],
) -> dict[str, Any]:
    fields = body.model_dump(exclude_none=True)
    if not fields:
        raise ValidationError("at least one field must be provided")
    import hashlib
    from app.modules.customer_care.api.kb_csat import _simple_chunk
    from app.modules.customer_care.models import CCKnowledgeChunk
    async with uow.transactional() as session:
        doc = (
            await session.execute(
                select(CCKnowledgeDocument).where(CCKnowledgeDocument.id == doc_id)
            )
        ).scalar_one_or_none()
        if doc is None:
            raise NotFoundError("KB doc not found")
        body_changed = False
        if "body" in fields:
            new_body = fields["body"]
            new_digest = hashlib.sha256(new_body.encode("utf-8")).hexdigest()
            if new_digest != doc.body_sha256:
                doc.body = new_body
                doc.body_sha256 = new_digest
                body_changed = True
        for k in ("title", "is_active", "language"):
            if k in fields:
                setattr(doc, k, fields[k])
        if body_changed:
            # Delete old chunks + re-create
            await session.execute(
                _t("DELETE FROM cc_knowledge_chunks WHERE document_id = :d"),
                {"d": doc_id},
            )
            chunks = _simple_chunk(doc.body or "")
            for i, ch in enumerate(chunks):
                session.add(
                    CCKnowledgeChunk(
                        document_id=doc.id, position=i, text_body=ch,
                        text_hash=hashlib.sha256(ch.encode("utf-8")).hexdigest(),
                        token_count=len(ch) // 4,
                    )
                )
            doc.chunk_count = len(chunks)
            doc.embedding_model = None
            doc.embedding_dim = None
            doc.indexed_at = datetime.now(timezone.utc)
        doc.updated_at = datetime.now(timezone.utc)
        await record_audit(
            actor=principal,
            action="customer_care.kb.updated",
            resource_type="cc_knowledge_documents",
            resource_id=doc_id,
            metadata={"fields": list(fields.keys()), "body_changed": body_changed},
        )
    return {"id": str(doc_id), "body_changed": body_changed, "fields": list(fields.keys())}


