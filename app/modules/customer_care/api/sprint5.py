@router.get(
    "/integrations/templates",
    summary="List Meta-approved WhatsApp templates for this WABA",
    dependencies=[Depends(requires_permission(_ADMIN))],
)
async def list_meta_templates() -> dict[str, Any]:
    """Hits Meta Graph API to enumerate approved templates. Returns
    empty list + reason if creds missing.
    """
    import os
    import httpx
    cfg = cc_settings()
    waba_id = os.environ.get("WHATSAPP_WABA_ID")
    if not (cfg.whatsapp_access_token and waba_id):
        return {
            "ok": False,
            "reason": "missing WHATSAPP_ACCESS_TOKEN or WHATSAPP_WABA_ID",
            "templates": [],
        }
    url = f"https://graph.facebook.com/{cfg.whatsapp_api_version}/{waba_id}/message_templates"
    try:
        async with httpx.AsyncClient(timeout=15.0) as client:
            r = await client.get(
                url, headers={"Authorization": f"Bearer {cfg.whatsapp_access_token}"},
            )
            r.raise_for_status()
            data = r.json().get("data") or []
            return {
                "ok": True,
                "templates": [
                    {
                        "name": t.get("name"),
                        "status": t.get("status"),
                        "language": t.get("language"),
                        "category": t.get("category"),
                    }
                    for t in data
                ],
            }
    except httpx.HTTPError as e:
        return {"ok": False, "reason": f"meta_api_error: {e}", "templates": []}


# ============================================================== SLA pause / resume
class SlaPauseRequest(StrictModel):
    reason: str = Field(..., min_length=1, max_length=500)


@router.post(
    "/conversations/{conv_id}/sla/pause",
    summary="Pause SLA timers (e.g. waiting on customer for info)",
    dependencies=[Depends(requires_permission(_AGENT))],
)
async def sla_pause(
    conv_id: Annotated[UUID, Path(...)],
    body: SlaPauseRequest,
    uow: Annotated[UnitOfWork, Depends(get_uow)],
    principal: Annotated[Principal, Depends(get_current_principal)],
) -> dict[str, Any]:
    async with uow.transactional() as session:
        r = await session.execute(
            _t(
                "UPDATE cc_conversations SET sla_paused_at = now(), sla_paused_reason = :r "
                "WHERE id = :c AND sla_paused_at IS NULL RETURNING id"
            ),
            {"c": conv_id, "r": body.reason},
        )
        if r.first() is None:
            raise HTTPException(404, "Conversation not found or already paused")
        await record_audit(
            actor=principal,
            action="customer_care.sla.paused",
            resource_type="cc_conversation", resource_id=conv_id,
            metadata={"reason": body.reason},
        )
    return {"id": str(conv_id), "sla_paused": True, "reason": body.reason}


@router.post(
    "/conversations/{conv_id}/sla/resume",
    summary="Resume SLA timers + shift due dates by paused duration",
    dependencies=[Depends(requires_permission(_AGENT))],
)
async def sla_resume(
    conv_id: Annotated[UUID, Path(...)],
    uow: Annotated[UnitOfWork, Depends(get_uow)],
    principal: Annotated[Principal, Depends(get_current_principal)],
) -> dict[str, Any]:
    """Shifts both due dates forward by (now - sla_paused_at) so the
    agent gets full remaining time after resume."""
    async with uow.transactional() as session:
        r = await session.execute(
            _t(
                "UPDATE cc_conversations "
                "SET sla_first_response_due_at = "
                "      CASE WHEN sla_first_response_due_at IS NOT NULL THEN "
                "           sla_first_response_due_at + (now() - sla_paused_at) "
                "      ELSE NULL END, "
                "    sla_resolution_due_at = "
                "      CASE WHEN sla_resolution_due_at IS NOT NULL THEN "
                "           sla_resolution_due_at + (now() - sla_paused_at) "
                "      ELSE NULL END, "
                "    sla_paused_at = NULL, "
                "    sla_paused_reason = NULL "
                "WHERE id = :c AND sla_paused_at IS NOT NULL RETURNING id"
            ),
            {"c": conv_id},
        )
        if r.first() is None:
            raise HTTPException(404, "Conversation not found or not paused")
        await record_audit(
            actor=principal,
            action="customer_care.sla.resumed",
            resource_type="cc_conversation", resource_id=conv_id,
        )
    return {"id": str(conv_id), "sla_paused": False}


# ============================================================== Per-agent rate limit
_agent_rate_buckets: dict[str, deque] = {}
import os as _os
_AGENT_RATE_LIMIT = int(_os.environ.get("CC_AGENT_RATE_LIMIT_PER_MIN", "120"))


def agent_rate_check(agent_id: UUID) -> tuple[bool, int]:
    """Used by router.send_message — returns (allowed, count_in_window)."""
    now = time.monotonic()
    window_start = now - 60.0
    key = str(agent_id)
    q = _agent_rate_buckets.setdefault(key, deque(maxlen=_AGENT_RATE_LIMIT * 2))
    while q and q[0] < window_start:
        q.popleft()
    if len(q) >= _AGENT_RATE_LIMIT:
        return False, len(q)
    q.append(now)
    return True, len(q) + 1


@router.get(
    "/me/rate-limit",
    summary="Inspect my current rate-limit bucket usage",
    dependencies=[Depends(requires_permission(_AGENT))],
)
async def my_rate_limit(
    principal: Annotated[Principal, Depends(get_current_principal)],
) -> dict[str, Any]:
    key = str(principal.user_id)
    q = _agent_rate_buckets.get(key)
    used = len(q) if q else 0
    return {
        "limit_per_min": _AGENT_RATE_LIMIT,
        "used": used,
        "remaining": max(0, _AGENT_RATE_LIMIT - used),
    }


